package com.intellego.parquearauco.entities;

import com.intellego.parquearauco.security.entities.BasicEntity;


public class MallEntity extends BasicEntity{

	private Integer id;
	private String name;
	private String idMallSap;
	private String financeCompany;
	private String subProjectManager;
	private String imageUuid;
	private CountryEntity country;	
	private MallTypeEntity mallType;
	private String nameImage;

	public MallEntity(){
	}	

	public MallEntity(Integer id, String name, String idMallSap,
			String financeCompany, String subProjectManager,
			CountryEntity country, MallTypeEntity mallType, String imageUuid) {
		super();
		this.id = id;
		this.name = name;
		this.idMallSap = idMallSap;
		this.financeCompany = financeCompany;
		this.subProjectManager = subProjectManager;
		this.country = country;
		this.mallType = mallType;
		this.imageUuid = imageUuid;
		this.nameImage = nameImage;
	}


	public String getNameImage() {
		return nameImage;
	}

	public void setNameImage(String nameImage) {
		this.nameImage = nameImage;
	}

	public String getImageUuid() {
		return imageUuid;
	}

	public void setImageUuid(String imageUuid) {
		this.imageUuid = imageUuid;
	}

	public String getFinanceCompany() {
		return financeCompany;
	}


	public void setFinanceCompany(String financeCompany) {
		this.financeCompany = financeCompany;
	}


	public String getSubProjectManager() {
		return subProjectManager;
	}


	public void setSubProjectManager(String subProjectManager) {
		this.subProjectManager = subProjectManager;
	}


	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIdMallSap() {
		return idMallSap;
	}

	public void setIdMallSap(String idMallSap) {
		this.idMallSap = idMallSap;
	}

	public CountryEntity getCountry() {
		return country;
	}

	public void setCountry(CountryEntity country) {
		this.country = country;
	}

	public MallTypeEntity getMallType() {
		return mallType;
	}

	public void setMallType(MallTypeEntity mallType) {
		this.mallType = mallType;
	}

}
