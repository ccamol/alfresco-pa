package com.intellego.parquearauco.entities;


import java.util.Date;

import com.intellego.parquearauco.security.entities.BasicEntity;

public class DocumentsToCarryEntity extends BasicEntity{

	private Integer id;
	private TenderEntity tender;
	private String uuid;
	private String name;
	private String format;
	private String description;
	private Integer type;
	private String documentType;
	private Date createdDate;


	public DocumentsToCarryEntity() {
	}

	public DocumentsToCarryEntity(Integer id, TenderEntity tender,
			String uuid, String name, String format, String description, Integer type,
			String documentType, Date createdDate) {
		this.id = id;
		this.tender = tender;
		this.uuid = uuid;
		this.name = name;
		this.format = format;
		this.description = description;
		this.type = type;
		this.documentType = documentType;
		this.createdDate = createdDate;
	}

	
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}


	public TenderEntity getTender() {
		return tender;
	}

	public void setTender(TenderEntity tender) {
		this.tender = tender;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public String getDocumentType() {
		return documentType;
	}

	public void setDocumentType(String documentType) {
		this.documentType = documentType;
	}

	public Date getCreatedDate() {
		return createdDate;
	}

	public void setCreatedDate(Date createdDate) {
		this.createdDate = createdDate;
	}
	
	


}
