package com.intellego.parquearauco.entities;

import java.util.Date;

import com.intellego.parquearauco.security.entities.BasicEntity;

public class ThirdPartySharedDocumentEntity extends BasicEntity {
	
	private Integer id;
	private String uuidDocument;
	private String thirdPartyName;
	private ProjectStagesEntity projectStages;
	private ArchitectureProjectEntity architectureProject;
	private Date sharedDate;
	
	
public ThirdPartySharedDocumentEntity() {
		
	}
	
	public ThirdPartySharedDocumentEntity(Integer id, String uuidDocument, 
			String thirdPartyName, ProjectStagesEntity projectStages,
			ArchitectureProjectEntity architectureProject, Date sharedDate) {
		
		this.id = id;
		this.uuidDocument = uuidDocument;
		this.thirdPartyName = thirdPartyName;
		this.projectStages = projectStages;
		this.architectureProject = architectureProject;
		this.sharedDate = sharedDate;
		
	}
	
	
	

	public ProjectStagesEntity getProjectStages() {
		return projectStages;
	}

	public void setProjectStages(ProjectStagesEntity projectStages) {
		this.projectStages = projectStages;
	}

	public ArchitectureProjectEntity getArchitectureProject() {
		return architectureProject;
	}

	public void setArchitectureProject(ArchitectureProjectEntity architectureProject) {
		this.architectureProject = architectureProject;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getUuidDocument() {
		return uuidDocument;
	}

	public void setUuidDocument(String uuidDocument) {
		this.uuidDocument = uuidDocument;
	}

	public String getThirdPartyName() {
		return thirdPartyName;
	}

	public void setThirdPartyName(String thirdPartyName) {
		this.thirdPartyName = thirdPartyName;
	}

	public Date getSharedDate() {
		return sharedDate;
	}

	public void setSharedDate(Date sharedDate) {
		this.sharedDate = sharedDate;
	}
}
