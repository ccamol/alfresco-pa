package com.intellego.parquearauco.entities;

import java.util.Date;

import com.intellego.parquearauco.security.entities.BasicEntity;

public class AwardApplicantTenderEntity extends BasicEntity {

	private Integer id;
	private TenderEntity tender;
	private String userName;
	private Date awardDate;

	public AwardApplicantTenderEntity() {
	}

	public AwardApplicantTenderEntity(Integer id, TenderEntity tender, ApplicantEntity applicant, 
			String userName, Date awardDate) {
		super();
		this.id = id;
		this.tender = tender;
		this.userName = userName;
		this.awardDate = awardDate;
	}


	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public TenderEntity getTender() {
		return tender;
	}

	public void setTender(TenderEntity tender) {
		this.tender = tender;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public Date getAwardDate() {
		return awardDate;
	}

	public void setAwardDate(Date awardDate) {
		this.awardDate = awardDate;
	}


}
