package com.intellego.parquearauco.entities;

import java.util.Date;

import com.intellego.parquearauco.security.entities.BasicEntity;

public class AnswerEntity extends BasicEntity {

	private Integer id;
	private String author;
	private String answer;
	private QuestionEntity question;
	private Date answerDate;

	public AnswerEntity() {

	}

	public AnswerEntity(Integer id, String author, String answer, 
			QuestionEntity question, Date answerDate) {
		super();
		this.id = id;
		this.author = author;
		this.answer = answer;
		this.question = question;
		this.answerDate = answerDate;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getAnswer() {
		return answer;
	}

	public void setAnswer(String answer) {
		this.answer = answer;
	}

	public QuestionEntity getQuestion() {
		return question;
	}

	public void setQuestion(QuestionEntity question) {
		this.question = question;
	}

	public Date getAnswerDate() {
		return answerDate;
	}

	public void setAnswerDate(Date answerDate) {
		this.answerDate = answerDate;
	}
	
	

}
