package com.intellego.parquearauco.security.entities;

import java.io.Serializable;

public class AreaEntity extends BasicEntity implements Serializable{

	private static final long serialVersionUID = 1L;
	
	Integer id;
	String site;
	
	public AreaEntity(){
		this.id=null;
		this.site=new String();
	}
	
	public AreaEntity(Integer id, String site){
		this.id=id;
		this.site=site;
	}
	
	public Integer getId() {
		return id;
	}
	
	public void setId(Integer id) {
		this.id = id;
	}
	
	public String getSite() {
		return site;
	}
	
	public void setSite(String site) {
		this.site = site;
	}
	
}
